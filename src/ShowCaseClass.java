public class ShowCaseClass {

    private int age;
    private String good;

    public ShowCaseClass(int age, String good) {
        this.age = age;
        this.good = good;
    }

    public ShowCaseClass(int age) {
        this.age = age;
    }

    public ShowCaseClass(String good) {
        this.good = good;
    }
}
